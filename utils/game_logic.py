def _counter_sentence(item_name, counters, threat):
    reasons = []

    if "crit" in counters and threat["crit_count"] >= 2:
        reasons.append("the enemy team has multiple critical-strike threats")

    if "ad" in counters and threat["ad_count"] >= 3:
        reasons.append("the enemy team is heavy physical damage")

    if "ap" in counters and threat["ap_count"] >= 3:
        reasons.append("the enemy team has strong magic damage")

    if "auto_attack" in counters and threat["auto_count"] >= 2:
        reasons.append("several enemies rely on basic attacks")

    if "healing" in counters and threat["healing_count"] >= 1:
        reasons.append("the enemy team has healing or shielding tools")

    if "tank" in counters:
        reasons.append("it helps against durable targets")

    if "burst" in counters:
        reasons.append("it helps survive burst damage")

    if "dive" in counters:
        reasons.append("it helps when enemies can dive onto you")

    if not reasons:
        return ""

    return "This is useful here because " + _join_list(reasons[:3]) + "."


def explain_item(item_name, raw_row):
    champion = raw_row.get("champion", "")
    lane = raw_row.get("lane", "")
    time_bucket = raw_row.get("time_bucket", "mid")
    role = get_champion_role(champion)
    threat = analyze_enemy_team(raw_row)

    metadata = ITEM_METADATA.get(item_name, {})
    stats = metadata.get("stats", [])
    effects = metadata.get("effects", [])
    counters = metadata.get("counters", [])

    if time_bucket == "early":
        phase = "early game"
    elif time_bucket == "mid":
        phase = "mid game"
    else:
        phase = "late game"

    intro = f"{item_name} is recommended for {champion}"

    if lane and lane != "NONE":
        intro += f" in the {lane} lane"

    intro += f" during the {phase}."

    sentences = [intro]

    counter_sentence = _counter_sentence(item_name, counters, threat)
    effect_sentence = _effect_sentence(item_name, effects)
    stat_sentence = _stat_sentence(item_name, stats)

    if counter_sentence:
        sentences.append(counter_sentence)

    if effect_sentence:
        sentences.append(effect_sentence)

    if stat_sentence and len(sentences) < 4:
        sentences.append(stat_sentence)

    if len(sentences) == 1:
        if role == "tank":
            sentences.append("This item supports tank durability and frontline value.")
        elif role == "adc":
            sentences.append("This item supports damage output and carry potential.")
        elif role == "mage":
            sentences.append("This item supports ability damage and safer teamfighting.")
        else:
            sentences.append("The neural network selected this item based on similar historical match states.")

    return " ".join(sentences[:4])